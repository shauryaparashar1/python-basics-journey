# ---- Examples ----

# String
city = "Noida"  

# Float
temperature = 35.1   

# Integer
humidity = 50        

# Boolean
is_raining = False   

print("City:", city)
print("Temperature:", temperature, "°C")                                
print("Humidity:", humidity, "%")
print("Raining:", is_raining)

# ---- Type Checking ----
print(type(city))
print(type(temperature))
print(type(humidity))
print(type(is_raining))

