# Arithmetic operator
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison Operators
x = 5
y = 10

print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)

# Logical Operator
is_student = True
has_id = False

print("AND:", is_student and has_id)
print("OR:", is_student or has_id)
print("NOT:", not is_student)

# Assignment Operators
num = 5

num += 2   # num = num + 2
print("After += :", num)

num -= 1   # num = num - 1
print("After -= :", num)

num *= 3   # num = num * 3
print("After *= :", num)

